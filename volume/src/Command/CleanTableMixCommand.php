    protected function execute(InputInterface $input, OutputInterface $output)
    {
        $this->input = $input;
        $this->output = $output;

        $totalTimeExec = 0;
        $this->mysql = $this->em->getConnection() ;

        $fp = fopen(__DIR__.'/../../var/log/app-mix-clean.log', 'w');
        $fpError = fopen(__DIR__.'/../../var/log/app-mix-clean-errors.log', 'w');

        $slugify = new Slugify();
        $lastFile = $this->em->getRepository(Audiofile::class)->findOneBy([], ['id' => 'desc']);
        if ($lastFile !== null)
            $lastId = $lastFile->getId();
        else
            $lastId = 0;
        


        $mixs = $this->mysql->fetchAll('select * from les_mix ORDER BY id ASC');
        $nMixs = count($mixs);
        $this->output->writeln("<info>avant boucle $nMixs</info>");
        
        foreach ($mixs as $key => $value) {

            //if ($key <= 38) continue;

            $lastFile = $this->em->getRepository(Audiofile::class)->findOneBy([], ['id' => 'desc']);
            if ($lastFile !== null)
                $lastId = $lastFile->getId();
            else
                $lastId = 0;

            // if ($key > 5) break;
            $lastId++;
            // $this->output->writeln("<info>$lastId <= 13</info>");
            
            $picture = basename($value['image']);
            $audioName = basename($value['lienAudio']);

            $size = ($this->getFilesize($audioName) !== false) ? $this->getFilesize($audioName) : 0; 

            
            
            if ($size) {
                
                $newName = str_replace('-mp3', '.mp3', $slugify->slugify($lastId.'-'.$audioName));

                $newPicture = $slugify->slugify($lastId.'-'.$picture);
                $newPicture = str_replace('-jpg', '.jpg', $newPicture);
                $newPicture = str_replace('-gif', '.gif', $newPicture);
                $newPicture = str_replace('-jpeg', '.jpeg', $newPicture);
                $newPicture = str_replace('-png', '.png', $newPicture);
                
                $dir_source = __DIR__."/../../old_mix/".$audioName;
                $dir_dest =   __DIR__."/../../public/static/mix/".$newName;
                
                if (file_exists($dir_source)) {
                        // $this->output->writeln("<info>$audioName Existe</info>");
                        $time_start = microtime(true);
                        // $output = shell_exec('cp "'.$dir_source.'" "'.$dir_dest.'"');
                        // $this->output->writeln("<info>$output</info>");
                        if (rename($dir_source, $dir_dest)) {

                            $time_end = microtime(true);
                            $time = $time_end - $time_start;

                            $totalTimeExec += $time;
                            $infos = "Copie $key sur $nMixs ok: ".basename($dir_dest)." ".((int)($size/1000000))."Mo en ".((int)$time)."s. ".((int)(($size/1000000)/($time)))."Mo/s. Temps total écoulé ".((int)$totalTimeExec)." s";

                            $this->output->writeln("<info>$infos</info>");
                            // fichier log
                            fwrite($fp, $infos."\n");

                            $audioFile = new Audiofile();
                            $audioFile
                            ->setPseudo($value['pseudo'])
                            ->setOldFilename($audioName)
                            ->setFilename($newName)
                            ->setDj($value['nom_DJ'])
                            ->setGroup($value['groupe'])
                            ->setTitle($value['titre'])
                            ->setPicture($newPicture)
                            ->setOldPictureName($picture)
                            ->setDescription($value['commentaire'])
                            ->setGenre($value['genre'])
                            ->setPublish(1)
                            ->setFilesize($size)
                            ->setFileCreatedAt( date_timestamp_set(new \Datetime(), $this->getFileDate($audioName)) )
                            ;

                            try {
                                $this->em->persist($audioFile);
                                $this->em->flush();
                            } catch (\Throwable $th) {
                                // throw new \Exception("Mysql Erreur ".$th, 1);
                                $this->output->writeln("<error>Fichier non enregistré en db: $dir_dest</error>");
                            }
                            


                        }else{
                            $lastId--;
                            fwrite($fpError, "Fichier non copier: $dir_dest"."\n");
                            $this->output->writeln("<error>Fichier non copier: $dir_dest</error>");
                        }
                } else {
                    $lastId--;
                    fwrite($fpError, "$audioName n'existe pas"."\n");
                    $this->output->writeln("<error>$audioName n'existe pas</error>");
                }


                    
            }else {
                $lastId--;
            }

        }

        
        fwrite($fp, "Fin du programme en ".((int)$totalTimeExec)." secondes pour $nMixs tracks"."\n");
        fclose($fp);
        fclose($fpError);

        $this->output->writeln("<info> Fin du programme en ".((int)$totalTimeExec)." secondes pour $nMixs tracks</info>");

        return 0;
    }




